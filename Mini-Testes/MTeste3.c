int digits(int x) { return (x == 0) ? 1 : (int)log10(x) + 1; }

int ordenaInt(int num) {
    int count = digits(num);
    int v[count];
    for (int i = 0; i < count; i++) {
        v[i] = num % 10;
        num /= 10;
    }

    int ind[10];
    for (int i = 0; i < 10; i++)
        ind[i] = 0;

    // counting sort
    for (int i = 0; i < count; i++)
        ind[v[i]]++;

    int k = 0;
    // coloca o array ordenado do maior para o menor
    for (int i = 9; i >= 0; i--)
        for (int j = 0; j < ind[i]; j++)
            v[k++] = i;

    int final = 0;
    for (int i = 0; i < count; i++)
        final += v[i] * pow(10, i);

    return final;
}

void changeArray(int array[], int N) {
    for (int i = 0; i < N; i++)
        array[i] = ordenaInt(array[i]);
}

int mostFrequent(int arr[], int n) {
    int maxCount = 0, maxElement = 0;
    for (int i = 0; i < n; i++) {
        int count = 1;
        for (int j = i + 1; j < n; j++) {
            if (arr[i] == arr[j])
                count++;
        }
        if (count > maxCount) {
            maxCount = count;
            maxElement = arr[i];
        }
    }
    return maxElement;
}

void swap(int v[], int i, int j) {
    int temp = v[i];
    v[i] = v[j];
    v[j] = temp;
}

void ordenaArray(int array[], int N) {
    int i, j;
    for (i = N; i > 0; i--) {
        for (j = 1; j < i; j++)
            if (array[j - 1] > array[j])
                swap(array, j - 1, j);
    }
}

